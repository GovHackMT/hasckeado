        <h3>Doe Sangue</h3>
        <p>Doar sangue é doar vida.</p> 
                    
                        
                        <p>As comunidades médica e científica, as Sociedades Internacionais da Cruz Vermelha e do Crescente Vermelho, a Organização Mundial da Saúde – OMS, a Organização Pan-Americana da Saúde – OPAS e outras Organizações Humanitárias não admitem e desencorajam, enfaticamente, a coleta de sangue de doadores remunerados. Em muitos países é ilegal esse tipo de doação de sangue, mas em alguns países a doação remunerada é permitida e em outros países ainda existe a doação remunerada na forma de mercado negro.</p> <br>
                        
