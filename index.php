                        <h2>Quem Samos</h2>
                        <br>
          
                        
                        
                        <p>Os Hemocentros são instituições públicas ou privadas que realizam atividades de hemoterapia e hematologia com o objetivo de fornecer sangue (seus componentes e hemoderivados),<br> preferencialmente, aos hospitais da rede pública (Quando se tratar de Hemocentros Públicos) e, preferencialmente, diversos da rede privada (Quando se tratar de Hemocentros Privados), além do atendimento ambulatorial das patologias relacionadas com o sangue.</p> </br>
             
                        <p>Os Hemocentros Públicos podem fornecer sangue aos hospitais privados quando houver necessidade e os Hemocentros Privados podem fornecer sangue aos hospitais da rede pública quando os bancos de sangue da rede pública estiverem com baixo estoque de sangue a ponto de impossibilitar o atendimento aos pacientes com doenças do sangue e os casos emergenciais que necessitam de sangue.</p> <br
                        
                        <p>Nos Hemocentros da Rede Pública a doação de sangue é voluntária, anônima, altruísta, não remunerada e o sangue é repassado de forma gratuita aos Hospitais Públicos e aos Prontos Socorros. Nos Hemocentros Privados a doação de sangue é voluntária, anônima, não remunerada e o sangue é repassado de forma gratuita aos Hospitais Privados – os Hemocentros Privados cobram o custo da coleta do sangue: os insumos utilizados, reagentes, materiais descartáveis e a mão-de-obra especializada, inclusive honorários médicos.</p> 
